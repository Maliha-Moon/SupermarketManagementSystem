    private void DeleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteButtonActionPerformed
        // TODO add your handling code here:
        int index = jTable1.getSelectedRow();
        if (index == -1) {
            JOptionPane.showMessageDialog(null, "Please select a category to delete.");
            return;
        }

        TableModel model = jTable1.getModel();
        String id = model.getValueAt(index, 0).toString();
        String name = model.getValueAt(index, 1).toString(); // Assuming name is at index 1
        int a = JOptionPane.showConfirmDialog(null, "Do you want to delete " + name + " Category", "Select", JOptionPane.YES_NO_OPTION);
        if (a == JOptionPane.YES_OPTION) {
            Category.delete(id);
            refreshTable(); // A method to refresh your table view
            JOptionPane.showMessageDialog(null, "Category deleted successfully!");
        }
    }//GEN-LAST:event_DeleteButtonActionPerformed

    // Method to refresh the table after deletion
    private void refreshTable() {

        ArrayList<Category> categories = Category.getAllCategory();
        updateTableModel(categories);
        
    }

// Method to update the table model with new data
    private void updateTableModel(ArrayList<Category> categories) {

        DefaultTableModel model = (DefaultTableModel) jTable1.getModel();
        model.setRowCount(0); // Clear existing data
        for (Category category : categories) {
            model.addRow(new Object[]{category.getId(), category.getName()});
        }
    }
