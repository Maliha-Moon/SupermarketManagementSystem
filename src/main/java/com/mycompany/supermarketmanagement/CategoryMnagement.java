    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        SwingWorker<Void, Void> worker = new SwingWorker<Void, Void>() {
            // @Override
            protected Void doInBackground() {
                ArrayList<Category> categoryList = Category.getAllCategory();
                DefaultTableModel tableModel = (DefaultTableModel) jTable1.getModel();
                tableModel.setRowCount(0); // clear the table before populating it again
                for (Category category : categoryList) {
                    tableModel.addRow(new Object[]{category.getId(), category.getName()});
                }
                return null;
            }
            //@Override

            protected void done() {
                jTable1.setModel((DefaultTableModel) jTable1.getModel());
            }
        };
        worker.execute();

    }//GEN-LAST:event_formComponentShown

