    private void jButton_signupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_signupActionPerformed

        String name = jTextField_name.getText();
        String user_name = jTextField_username.getText();
        String mail = jTextField_mail.getText();
        String pass1 = String.valueOf(jPasswordField_password.getPassword());
        String pass2 = String.valueOf(confirm_password.getPassword());

        if (emptyFields()) {
            if (!checkUserName(user_name)) {
                PreparedStatement st;
                ResultSet rs;
                String signUpUserQuery = "INSERT INTO supermarket_login.sign_up_info ( name,username, E_mail, password) VALUES (?,?,?,?)";

                try {

                    st = DatabaseConnection.getConnection().prepareStatement(signUpUserQuery);

                    st.setString(1, name);
                    st.setString(2, user_name);
                    st.setString(3, mail);
                    st.setString(4, pass1);

                    //rs = st.executeQuery();
                    if (st.executeUpdate() != 0) {
                        JOptionPane.showMessageDialog(null, "Your account has been successfully created!");

                        HomePage home = new HomePage();
                        home.setVisible(true);
                        home.pack();
                        home.setLocationRelativeTo(null);
                        home.setDefaultCloseOperation(JFrame.EXIT_ON_CLOSE);
                        // Close the current login form
                        this.dispose();
                    } else {
                        JOptionPane.showMessageDialog(null, " Something Went Wrong!");
                    }

                } catch (SQLException ex) {
                    // Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
                }
            }
        }
    }//GEN-LAST:event_jButton_signupActionPerformed

