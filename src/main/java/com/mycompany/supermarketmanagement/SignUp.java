    public boolean emptyFields() {

        String name = jTextField_name.getText();
        String user_name = jTextField_username.getText();
        String mail = jTextField_mail.getText();
        String pass1 = String.valueOf(jPasswordField_password.getPassword());
        String pass2 = String.valueOf(confirm_password.getPassword());

        //chech empty fields
        if (name.trim().equals("") || user_name.trim().equals("") || user_name.trim().equals("") || mail.trim().equals("")
                || pass1.trim().equals("") || pass2.trim().equals("") || !mail.matches(emailPattern)) {
            JOptionPane.showMessageDialog(null, "Please fill in the empty box to help us stock up on your information!");
            return false;
        }

        if (pass1.length() < 8) {
            JOptionPane.showMessageDialog(null, "Password must be at least 8 characters!");
        }

        if (pass1.length() > 32) {
            JOptionPane.showMessageDialog(null, "Password must be less than 32 characters!");
        }

        // if 2 password not matched
        if (!pass1.equals(pass2)) {
            JOptionPane.showMessageDialog(null, "Oops! Passwords: mismatched", "Confirm Password", 2);
            return false;
        } else {
            return true;
        }
    }

    // function to check if the username already exists or not
    public boolean checkUserName(String username) {

        PreparedStatement st;
        ResultSet rs;
        boolean is_username = false;

        String query = "SELECT * FROM supermarket_login.sign_up_info WHERE username = ?";

        try {

            st = DatabaseConnection.getConnection().prepareStatement(query);

            st.setString(1, username);
            rs = st.executeQuery();

            if (rs.next()) {
                is_username = true;
                JOptionPane.showMessageDialog(null, "Username's taken!", "Try a different username, please!", 2);
            }

        } catch (SQLException ex) {
            Logger.getLogger(SignUp.class.getName()).log(Level.SEVERE, null, ex);
        }

        return is_username;
    }

    public static ArrayList<User> getUserRecords() {
        ArrayList<User> list = new ArrayList<>();
        PreparedStatement st;
        ResultSet rs;

        String query = "SELECT * FROM supermarket_login.sign_up_info";
        try {
            st = DatabaseConnection.getConnection().prepareStatement(query);
            // st.setString(1, username);

            rs = st.executeQuery();
            while (rs.next()) {
                User user = new User();
                user.setId(rs.getInt("id"));
                user.setFullName(rs.getString("name"));
                user.setUserName(rs.getString("username"));
                user.setEmail(rs.getString("E_mail"));
                user.setStatus(rs.getString("user_status"));

                list.add(user);

            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }

        return list;
    }

    public static void changeStatus(String username, String status) {
        PreparedStatement st;
        ResultSet rs;

        String query = "UPDATE supermarket_login.sign_up_info SET user_status = ? WHERE username = ?";
        try {
            st = DatabaseConnection.getConnection().prepareStatement(query);
            st.setString(1, status); // Set the status parameter
            st.setString(2, username); // Set the username parameter

            // Execute the update
            int rowsUpdated = st.executeUpdate();
            if (rowsUpdated > 0) {
                System.out.println("User status updated successfully!");
            } else {
                System.out.println("No user found with the given username.");
            }
        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

